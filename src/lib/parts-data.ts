// Helper para obtener partes por categor√≠a
export function getPartsByCategory(category: string): any[] {
  switch (category) {
    case 'cpu': return CPUS;
    case 'mb': return MOTHERBOARDS;
    case 'ram': return RAMS;
    case 'gpu': return GPUS;
    case 'storage': return STORAGES;
    case 'psu': return PSUS;
    case 'case': return CASES;
    case 'cooler': return COOLERS;
    default: return [];
  }
}

export function getPartById(id: string): any | null {
  const all = [...CPUS, ...MOTHERBOARDS, ...RAMS, ...GPUS, ...STORAGES, ...PSUS, ...CASES, ...COOLERS];
  return all.find(p => p.id === id) || null;
}

